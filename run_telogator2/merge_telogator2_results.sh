#!/bin/bash

# ==============================================================================
# Dcription: Merge Telogator2 output files across multiple samples.
# ==============================================================================

# ------------------ Paths and Environment (Users must modify) -----------------
# 1. Set the root working directory
WORK_DIR="/path/to/your_workdir"
# ------------------------------------------------------------------------------

# Path to the sample list file
SAMPLE_LIST="${WORK_DIR}/sample_name_list_file"

OUT_FILE="${WORK_DIR}/merged_tlens_by_allele.tsv"

# The primary result file generated by Telogator2
TARGET_FILE="tlens_by_allele.tsv"

# Initialize a flag to ensure the header is only written once
HEADER_WRITTEN=0

# Clear or create the output file
> "${OUT_FILE}"

for individual in `cat ${SAMPLE_LIST}`
do
    # Define path for the individual's result based on the first script's output structure
    result_file="${WORK_DIR}/${individual}/${individual}_telo_out/${TARGET_FILE}"
    
    if [ -f "$result_file" ]; then
        if [ $HEADER_WRITTEN -eq 0 ]; then
            # Extract header, prepend 'sample', and write to output
            head -n 1 "$result_file" | awk '{print "sample\t" $0}' > "${OUT_FILE}"
            HEADER_WRITTEN=1
        fi
        # Skip header, prepend sample name, and append to output
        awk -v sample="${individual}" 'NR>1 {print sample "\t" $0}' "$result_file" >> "${OUT_FILE}"
    else
        echo "Warning: Target file not found at $result_file"
    fi
done

echo "Merge complete. Data saved to: ${OUT_FILE}"
